#!/usr/bin/python3
def roman_to_int(roman_string):
    if not roman_string:
        return 0
    elif not isinstance(roman_string, str):
        return 0
    else:
        rom_val = {
            'I': 1, 'V': 5, 'X': 10, 'L': 50, 'C': 100, 'D': 500, 'M': 1000}
        int_val = 0
        s = roman_string
        for i in range(len(s)):
            if i > 0 and rom_val[s[i]] > rom_val[s[i - 1]]:
                int_val += rom_val[s[i]] - 2 * rom_val[s[i - 1]]
            else:
                int_val += rom_val[s[i]]
        return int_val
